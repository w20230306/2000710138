package ExperimentOne;

public class IfElseTest {
        public static void main(String[] args) {
            int a=3;
            if(a==1)
                System.out.println("进行加法运算");
            else if(a==2)
                System.out.println("进行减法运算");
            else if(a==3)
                System.out.println("进行乘法运算");
            else
                System.out.println("进行除法运算");
        }
    }
